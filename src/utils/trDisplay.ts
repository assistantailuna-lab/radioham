export function trDisplay(value: unknown): string {
	let text = String(value ?? "");
	const escapeRegex = (value: string) => value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
	const applyPairs = (input: string, pairs: Array<[string, string]>, wholeWordOnly: boolean): string => {
		let output = input;
		for (const [from, to] of pairs) {
			if (!from) continue;
			if (wholeWordOnly && /^[A-Za-z\u00c7\u011e\u0130\u00d6\u015e\u00dc\u00e7\u011f\u0131\u00f6\u015f\u00fc]+$/.test(from)) {
				const pattern = new RegExp(`(?<![A-Za-z\u00c7\u011e\u0130\u00d6\u015e\u00dc\u00e7\u011f\u0131\u00f6\u015f\u00fc])${escapeRegex(from)}(?![A-Za-z\u00c7\u011e\u0130\u00d6\u015e\u00dc\u00e7\u011f\u0131\u00f6\u015f\u00fc])`, "g");
				output = output.replace(pattern, to);
				continue;
			}
			output = output.replaceAll(from, to);
		}
		return output;
	};

	text = applyPairs(text, BROKEN_REPAIRS, false);
	text = applyPairs(text, REPLACEMENTS, true);
	// Common mojibake sequences observed in mixed-encoding content.
	text = text
		.replaceAll("\u00c3\u00bc", "\u00fc")
		.replaceAll("\u00c3\u009c", "\u00dc")
		.replaceAll("\u00c3\u00b6", "\u00f6")
		.replaceAll("\u00c3\u0096", "\u00d6")
		.replaceAll("\u00c3\u00a7", "\u00e7")
		.replaceAll("\u00c3\u0087", "\u00c7")
		.replaceAll("\u00c4\u00b1", "\u0131")
		.replaceAll("\u00c4\u00b0", "\u0130")
		.replaceAll("\u00c5\u009f", "\u015f")
		.replaceAll("\u00c5\u009e", "\u015e")
		.replaceAll("\u00c4\u009f", "\u011f")
		.replaceAll("\u00c4\u009e", "\u011e");
	return text;
}

